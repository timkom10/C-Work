/* 
 * fitsBits - return 1 if x can be represented as an 
 *  n-bit, two's complement integer.
 *   1 <= n <= 32
 *   Examples: fitsBits(5,3) = 0, fitsBits(-4,3) = 1
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 15
 *   Rating: 2
 */
int fitsBits(int x, int n) {
  int f = n + (~0); // convert n to n-1
  int g = (x >> 31) & 1; //sign bit
  int mask = ~(g+~0); // convert signbit to mask
  x = x^mask; // xor to remove lead bits
  x = x>>(f); // shift x n-1 times
  return !x; //returns zero unless bits dont fit
}
