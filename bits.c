/* 
 * conditional - same as x ? y : z 
 *   Example: conditional(2,4,5) = 4
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 16
 *   Rating: 3
 */
int conditional(int x, int y, int z) {
  int mask = ~(!!x) + 1; // make mask where x is only 1s or 0s 
  return (z&~mask) | (mask&y); // return x & comp of mask or mask & y
}
/* 
 * rotateLeft - Rotate x to the left by n
 *   Can assume that 0 <= n <= 31
 *   Examples: rotateLeft(0x87654321,4) = 0x76543218
 *   Legal ops: ~ & ^ | + << >> !
 *   Max ops: 25
 *   Rating: 3 
 */
int rotateLeft(int x, int n) {
  int num;
  int mask;
  int rotate;

  num = 32 + ~n; // finds n - 32 num for rotation
  mask = ~((~0x00) << n); // mask shifted by rotation num
  rotate = (x >> num) >> 1; //rotating x using shift by n-32 number + 1
  rotate = mask&rotate;
  x = x << n; // make x only the rotated bytes
  return x | rotate; 
}
/* 
 * bitMask - Generate a mask consisting of all 1's 
 *   lowbit and highbit
 *   Examples: bitMask(5,3) = 0x38
 *   Assume 0 <= lowbit <= 31, and 0 <= highbit <= 31
 *   If lowbit > highbit, then mask should be all 0's
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 16
 *   Rating: 3
 */
int bitMask(int highbit, int lowbit) {
  int negone = ~0;
  int hibit = (negone << highbit) << 1; //hightbit right shift by -1 and  highbit + 1 bits 
  int lobit = negone << lowbit; // right  shift -1 by lowbit bits

  return(hibit ^ lobit) & lobit; //xor used to get the difference, lobit is a mask for when lowbit>highbit
}
/* 
 * bang - Compute !x without using !
 *   Examples: bang(3) = 0, bang(0) = 1
 *   Legal ops: ~ & ^ | + << >>
 *   Max ops: 12
 *   Rating: 4 
 */
int bang(int x) {
  int compX = ~x + 1; // need comp to find negative x
  int signbit = (x | compX) >> 31; // the signbit is x or compx shifted right
// by 31 because it becomes 0
  return signbit + 1;
}
